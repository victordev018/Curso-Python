# instrução "in" e "not in"

lista = [1, 2, 3, 4, 5]

print(1 in lista) # caso o 1 esteja na lista, será avaliada como True, outro caso, False
print(7 in lista) # caso o 7 esteja na lista, será avaliada como True, outro caso, False
print(9 not in lista) # caso 9 não esteja na lista, será avalidad como True, outro caso, False