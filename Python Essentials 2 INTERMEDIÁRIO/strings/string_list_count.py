# a função list(), a partir de seu argumento(string), cria uma lista com os elemnetos da string:

nome = 'João Dev'
letraSeparada = list(nome)
print(letraSeparada)

# a classe count(), conta qunatas vezes determinado  elemento aparece:
print("quantidad da letra 'o': ", letraSeparada.count('o'))  #output: 2