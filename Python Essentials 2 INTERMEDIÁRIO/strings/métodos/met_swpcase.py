# o método swapcase(), inverte todos os caracteres, maiúsculo vira minúsculo e vice-versa:
print("\nswapcasw()","I love labiras".swapcase())  #output: i LOVE LABIRAS

# o métod title(), muda a primeira letra de palavra para maiúscula:
print("title()","i love labiras".title())

# método upper(), subistitui todas as letras minúsculas para maiúsculas:
print("upper()", "abcdefghijk".upper())